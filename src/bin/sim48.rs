//! RustPIC 48 kHz channel simulator.
//!
//! Takes the 48 kHz WAV output of the TX binary, applies configurable channel
//! impairments, and writes a 48 kHz WAV suitable as input for the RX binary.
//! This lets you test the full TX→RX pipeline without a radio.
//!
//! # Impairment model
//!
//! ```text
//! TX.wav  ──► [silence offset]  ──► [noise]  ──► [clock warp]  ──► RX.wav
//! ```
//!
//! Two noise models are supported:
//!
//! * **AWGN** (default): white Gaussian noise.  Every subcarrier sees the
//!   same SNR.  Models an ideal linear channel.
//!
//! * **FM discriminator** (`--fm`): noise PSD ∝ f², generated by taking the
//!   first difference of white noise:  d\[n\] = w\[n\] − w\[n−1\].  This
//!   matches the FM discriminator noise floor that grows as the square of the
//!   subcarrier frequency.  With this model, SNR at 2531 Hz is 18 dB worse
//!   than SNR at 312 Hz for the same `--snr` setting.
//!
//! SNR is always defined as `10 log₁₀(P_signal / P_noise)` where both
//! powers are measured over the full audio bandwidth (0–24 kHz at 48 kHz).
//!
//! # Usage
//!
//! ```text
//! sim48 --input tx.wav --output rx.wav [OPTIONS]
//!
//! OPTIONS:
//!   --snr    <dB>   Target SNR in dB          (default: 30)
//!   --fm            FM discriminator f² noise  (default: AWGN)
//!   --ppm    <n>    Clock offset in ppm        (default: 0)
//!   --offset <n>    Prepend N silence samples  (default: 2000)
//!   --seed   <n>    RNG seed for reproducibility
//! ```

use std::io::Write as IoWrite;

use rand::{Rng, SeedableRng, rngs::StdRng};

// ── CLI ───────────────────────────────────────────────────────────────────────

struct Cfg {
    input:    String,
    output:   String,
    snr_db:   f32,
    fm_noise: bool,
    ppm:      f32,
    offset:   usize,
    seed:     Option<u64>,
    fm_chan:  bool,   // apply pre-emph → clip → de-emph pipeline
    fm_tau:   f32,    // pre-emph time constant in µs (75 = standard NBFM, matches GNU Radio default)
    fm_clip:  f32,    // hard-clip threshold in dB above pre-emph'd RMS (inf = no clip)
    audio_hp: f32,    // audio HPF cutoff in Hz (0 = off) — models radio mic HPF
    audio_lp: f32,    // audio LPF cutoff in Hz (0 = off) — models radio audio LPF
}

impl Default for Cfg {
    fn default() -> Self {
        Self {
            input:    String::new(),
            output:   String::new(),
            snr_db:   30.0,
            fm_noise: false,
            ppm:      0.0,
            offset:   2000,
            seed:     None,
            fm_chan:  false,
            fm_tau:   75.0,
            fm_clip:  f32::INFINITY,
            audio_hp: 0.0,
            audio_lp: 0.0,
        }
    }
}

fn die(msg: &str) -> ! {
    eprintln!("sim48: {msg}");
    std::process::exit(1);
}

fn parse_args() -> Cfg {
    let mut cfg  = Cfg::default();
    let args: Vec<String> = std::env::args().skip(1).collect();
    let mut i = 0;
    while i < args.len() {
        match args[i].as_str() {
            "--input"    => { i += 1; cfg.input  = args[i].clone(); }
            "--output"   => { i += 1; cfg.output = args[i].clone(); }
            "--snr"      => { i += 1; cfg.snr_db = args[i].parse::<f32>()
                .unwrap_or_else(|_| die("--snr expects a dB value")); }
            "--fm"       => { cfg.fm_noise = true; }
            "--ppm"      => { i += 1; cfg.ppm = args[i].parse::<f32>()
                .unwrap_or_else(|_| die("--ppm expects a number")); }
            "--offset"   => { i += 1; cfg.offset = args[i].parse::<usize>()
                .unwrap_or_else(|_| die("--offset expects an integer")); }
            "--seed"     => { i += 1; cfg.seed = Some(args[i].parse::<u64>()
                .unwrap_or_else(|_| die("--seed expects an integer"))); }
            "--fm-channel" => { cfg.fm_chan = true; }
            "--fm-tau"   => { i += 1; cfg.fm_tau = args[i].parse::<f32>()
                .unwrap_or_else(|_| die("--fm-tau expects µs value, e.g. 750")); }
            "--fm-clip"  => { i += 1; cfg.fm_clip = args[i].parse::<f32>()
                .unwrap_or_else(|_| die("--fm-clip expects dB above RMS, e.g. 10")); }
            "--audio-hp" => { i += 1; cfg.audio_hp = args[i].parse::<f32>()
                .unwrap_or_else(|_| die("--audio-hp expects Hz, e.g. 300")); }
            "--audio-lp" => { i += 1; cfg.audio_lp = args[i].parse::<f32>()
                .unwrap_or_else(|_| die("--audio-lp expects Hz, e.g. 3000")); }
            "--help"|"-h" => { print_help(); std::process::exit(0); }
            a => die(&format!("unknown argument: {a}  (try --help)")),
        }
        i += 1;
    }
    if cfg.input.is_empty()  { die("--input is required"); }
    if cfg.output.is_empty() { die("--output is required"); }
    cfg
}

