//! RustPIC 48 kHz channel simulator.
//!
//! Takes the 48 kHz WAV output of the TX binary, applies configurable channel
//! impairments, and writes a 48 kHz WAV suitable as input for the RX binary.
//! This lets you test the full TX→RX pipeline without a radio.
//!
//! # Impairment model
//!
//! ```text
//! TX.wav  ──► [silence offset]  ──► [noise]  ──► [clock warp]  ──► RX.wav
//! ```
//!
//! Two noise models are supported:
//!
//! * **AWGN** (default): white Gaussian noise.  Every subcarrier sees the
//!   same SNR.  Models an ideal linear channel.
//!
//! * **FM discriminator** (`--fm`): noise PSD ∝ f², generated by taking the
//!   first difference of white noise:  d\[n\] = w\[n\] − w\[n−1\].  This
//!   matches the FM discriminator noise floor that grows as the square of the
//!   subcarrier frequency.  With this model, SNR at 2531 Hz is 18 dB worse
//!   than SNR at 312 Hz for the same `--snr` setting.
//!
//! SNR is always defined as `10 log₁₀(P_signal / P_noise)` where both
//! powers are measured over the full audio bandwidth (0–24 kHz at 48 kHz).
//!
//! # Usage
//!
//! ```text
//! sim48 --input tx.wav --output rx.wav [OPTIONS]
//!
//! OPTIONS:
//!   --snr    <dB>   Target SNR in dB          (default: 30)
//!   --fm            FM discriminator f² noise  (default: AWGN)
//!   --ppm    <n>    Clock offset in ppm        (default: 0)
//!   --offset <n>    Prepend N silence samples  (default: 2000)
//!   --seed   <n>    RNG seed for reproducibility
//! ```

use std::io::Write as IoWrite;

use rand::{Rng, SeedableRng, rngs::StdRng};

// ── CLI ───────────────────────────────────────────────────────────────────────

struct Cfg {
    input:    String,
    output:   String,
    snr_db:   f32,
    fm_noise: bool,
    ppm:      f32,
    offset:   usize,
    seed:     Option<u64>,
}

impl Default for Cfg {
    fn default() -> Self {
        Self {
            input:    String::new(),
            output:   String::new(),
            snr_db:   30.0,
            fm_noise: false,
            ppm:      0.0,
            offset:   2000,
            seed:     None,
        }
    }
}

fn die(msg: &str) -> ! {
    eprintln!("sim48: {msg}");
    std::process::exit(1);
}

fn parse_args() -> Cfg {
    let mut cfg  = Cfg::default();
    let args: Vec<String> = std::env::args().skip(1).collect();
    let mut i = 0;
    while i < args.len() {
        match args[i].as_str() {
            "--input"    => { i += 1; cfg.input  = args[i].clone(); }
            "--output"   => { i += 1; cfg.output = args[i].clone(); }
            "--snr"      => { i += 1; cfg.snr_db = args[i].parse::<f32>()
                .unwrap_or_else(|_| die("--snr expects a dB value")); }
            "--fm"       => { cfg.fm_noise = true; }
            "--ppm"      => { i += 1; cfg.ppm = args[i].parse::<f32>()
                .unwrap_or_else(|_| die("--ppm expects a number")); }
            "--offset"   => { i += 1; cfg.offset = args[i].parse::<usize>()
                .unwrap_or_else(|_| die("--offset expects an integer")); }
            "--seed"     => { i += 1; cfg.seed = Some(args[i].parse::<u64>()
                .unwrap_or_else(|_| die("--seed expects an integer"))); }
            "--help"|"-h" => { print_help(); std::process::exit(0); }
            a => die(&format!("unknown argument: {a}  (try --help)")),
        }
        i += 1;
    }
    if cfg.input.is_empty()  { die("--input is required"); }
    if cfg.output.is_empty() { die("--output is required"); }
    cfg
}

fn print_help() {
    println!("sim48 — RustPIC 48 kHz channel simulator");
    println!();
    println!("USAGE: sim48 --input tx.wav --output rx.wav [OPTIONS]");
    println!();
    println!("OPTIONS:");
    println!("  --input  <tx.wav>  Input WAV (48 kHz, output of tx binary) [required]");
    println!("  --output <rx.wav>  Output WAV (48 kHz, input for rx binary) [required]");
    println!("  --snr    <dB>      SNR in dB (default: 30)");
    println!("  --fm               FM discriminator f²-shaped noise (default: AWGN)");
    println!("  --ppm    <n>       Clock offset in ppm (default: 0)");
    println!("  --offset <n>       Prepend N silence samples (default: 2000)");
    println!("  --seed   <n>       RNG seed for reproducibility");
    println!();
    println!("NOISE MODELS:");
    println!("  AWGN (default): white noise, same SNR on all subcarriers.");
    println!("  FM   (--fm):    f²-shaped noise. SNR at 2531 Hz is 18 dB worse");
    println!("                  than at 312 Hz — same total power as AWGN.");
    println!();
    println!("EXAMPLES:");
    println!("  sim48 --input tx.wav --output rx.wav --snr 20");
    println!("  sim48 --input tx.wav --output rx.wav --snr 25 --fm --ppm 50");
}

// ── Main ──────────────────────────────────────────────────────────────────────

fn main() {
    let cfg = parse_args();

    // ── Load input WAV ────────────────────────────────────────────────────────
    let (tx_samples, rate) = read_wav_mono(&cfg.input)
        .unwrap_or_else(|e| die(&format!("cannot read '{}': {e}", cfg.input)));

    if rate != 48_000 && rate != 8_000 {
        die(&format!("unsupported sample rate {rate} Hz (expected 48000 or 8000)"));
    }

    let n = tx_samples.len();
    println!("sim48");
    println!("  Input    : {} ({} samples @ {} Hz, {:.1} s)",
        cfg.input, n, rate, n as f64 / rate as f64);
    println!("  Noise    : {} @ {:.1} dB SNR",
        if cfg.fm_noise { "FM f²" } else { "AWGN" }, cfg.snr_db);
    if cfg.ppm != 0.0 {
        println!("  Clock    : {:+.1} ppm", cfg.ppm);
    }
    if cfg.offset > 0 {
        println!("  Offset   : {} samples silence prepended", cfg.offset);
    }

    // ── RNG ───────────────────────────────────────────────────────────────────
    let mut rng: StdRng = match cfg.seed {
        Some(s) => StdRng::seed_from_u64(s),
        None    => StdRng::from_entropy(),
    };

    // ── Noise sigma from signal RMS ───────────────────────────────────────────
    // SNR = P_signal / P_noise  →  sigma = sqrt(P_signal / 10^(snr/10))
    let signal_power = if n > 0 {
        tx_samples.iter().map(|&x| x * x).sum::<f32>() / n as f32
    } else {
        1.0
    };
    let sigma = f32::sqrt(signal_power / 10f32.powf(cfg.snr_db / 10.0));

    // ── Generate noise vector ─────────────────────────────────────────────────
    let noise: Vec<f32> = if cfg.fm_noise {
        fm_discriminator_noise(n, sigma, &mut rng)
    } else {
        awgn(n, sigma, &mut rng)
    };

    // Measure actual noise power for verification.
    let actual_noise_power = noise.iter().map(|&x| x * x).sum::<f32>() / n as f32;
    let actual_snr = 10.0 * (signal_power / actual_noise_power.max(1e-15)).log10();
    println!("  Actual   : {:.2} dB SNR (P_sig={:.2e}, P_noise={:.2e})",
        actual_snr, signal_power, actual_noise_power);

    // ── Prepend silence + add noise ───────────────────────────────────────────
    let mut out: Vec<f32> = Vec::with_capacity(cfg.offset + n);

    // Silence prefix: noise only (no signal).
    for _ in 0..cfg.offset {
        out.push(box_muller(&mut rng) * sigma);
    }

    // Signal + noise.
    for (s, e) in tx_samples.iter().zip(noise.iter()) {
        out.push(s + e);
    }

    // ── Clock offset ──────────────────────────────────────────────────────────
    // rate_rx > 1: receiver clock faster → audio plays back faster → shorter
    let out = if cfg.ppm.abs() > 0.0 {
        let rate_rx = 1.0 + cfg.ppm / 1_000_000.0;
        linear_resample(&out, rate_rx)
    } else {
        out
    };

    // ── Write output WAV ──────────────────────────────────────────────────────
    // Normalise to ±1.0 then convert to i16 (matching the TX binary's approach).
    let peak = out.iter().map(|&x| x.abs()).fold(0f32, f32::max);
    let scale = if peak > 1e-9 { 1.0 / peak } else { 1.0 };
    let samples_i16: Vec<i16> = out.iter()
        .map(|&x| (x * scale * 32767.0).round().clamp(-32768.0, 32767.0) as i16)
        .collect();

    write_wav_mono(&cfg.output, &samples_i16, rate)
        .unwrap_or_else(|e| die(&format!("cannot write '{}': {e}", cfg.output)));

    let n_out = samples_i16.len();
    println!("  Output   : {} ({} samples, {:.1} s)",
        cfg.output, n_out, n_out as f64 / rate as f64);
}

// ── Noise generators ─────────────────────────────────────────────────────────

/// White Gaussian noise at the given standard deviation.
fn awgn(n: usize, sigma: f32, rng: &mut impl Rng) -> Vec<f32> {
    (0..n).map(|_| box_muller(rng) * sigma).collect()
}

/// FM-discriminator f²-shaped noise at 48 kHz.
///
/// Models the FM receiver's discriminator noise floor, whose PSD ∝ f².
///
/// ## Implementation
///
/// 1. Generate noise at **8 kHz** (the native OFDM sample rate) using a
///    first-difference filter:  d[n] = (w[n] − w[n−1]) / √2.
///    This gives PSD ∝ f² in the audio band 0–4 kHz.
///
/// 2. ZOH-upsample to 48 kHz by repeating each sample 6 times — exactly
///    the same path as the TX binary.
///
/// Generating at 8 kHz is critical: if we used first-difference at 48 kHz,
/// the f² shape would peak at 24 kHz, far above the audio band, and the
/// noise power in our signal band (312–2531 Hz) would be negligible.  The
/// FM radio's audio filter removes everything above ~3 kHz, so only the
/// 0–4 kHz portion of the noise matters.
///
/// After the RX binary's 13-tap FIR downsampler removes the 8 kHz images
/// created by ZOH upsampling, the remaining noise has a clean f² shape in
/// 0–4 kHz — matching the FM discriminator model.
///
/// ## SNR interpretation
///
/// The specified `sigma` is calibrated so that the **total** noise power
/// equals the total signal power divided by 10^(SNR/10).  However, because
/// the noise PSD ∝ f², the per-subcarrier SNR varies:
///
/// ```text
/// SNR(f_k) ≈ SNR_average − 14 dB  …  SNR_average + 14 dB
/// (lowest carrier 312 Hz better, highest 2531 Hz worse)
/// ```
///
/// Typical result: with `--fm --snr 14`, the equaliser will report a
/// per-subcarrier SNR range of approximately 0–28 dB (average ~14 dB),
/// reproducing OTA behaviour on a 30 dB FM link.
fn fm_discriminator_noise(n48: usize, sigma: f32, rng: &mut impl Rng) -> Vec<f32> {
    const UP: usize = 6; // 48 kHz / 8 kHz
    let n8k = n48.div_ceil(UP) + 1; // a few extra so trim is safe

    // Generate first-difference noise at 8 kHz.
    let mut prev = box_muller(rng) * sigma;
    let noise_8k: Vec<f32> = (0..n8k)
        .map(|_| {
            let curr = box_muller(rng) * sigma;
            let d    = (curr - prev) * std::f32::consts::FRAC_1_SQRT_2;
            prev = curr;
            d
        })
        .collect();

    // ZOH upsample 8 kHz → 48 kHz: each 8 kHz sample repeated UP times.
    noise_8k.iter()
        .flat_map(|&x| std::iter::repeat(x).take(UP))
        .take(n48)
        .collect()
}

/// Box–Muller transform → single standard-normal sample.
#[inline]
fn box_muller(rng: &mut impl Rng) -> f32 {
    let u1: f32 = rng.gen::<f32>().max(1e-10);
    let u2: f32 = rng.gen::<f32>();
    (-2.0 * u1.ln()).sqrt() * (2.0 * std::f32::consts::PI * u2).cos()
}

// ── Clock-offset resampler ────────────────────────────────────────────────────

/// Linear-interpolation resampler.
///
/// `rate > 1.0` → output shorter (receiver clock faster than TX).
/// `rate < 1.0` → output longer  (receiver clock slower).
fn linear_resample(samples: &[f32], rate: f32) -> Vec<f32> {
    if (rate - 1.0).abs() < 1e-9 {
        return samples.to_vec();
    }
    let n_in  = samples.len();
    let n_out = (n_in as f32 / rate).ceil() as usize;
    (0..n_out).map(|i| {
        let pos  = i as f32 * rate;
        let idx  = pos as usize;
        let frac = pos - idx as f32;
        if idx + 1 >= n_in {
            *samples.last().unwrap_or(&0.0)
        } else {
            samples[idx] * (1.0 - frac) + samples[idx + 1] * frac
        }
    }).collect()
}

// ── WAV I/O ───────────────────────────────────────────────────────────────────

/// Read a WAV file and return mono f32 samples + sample rate.
/// Stereo files are averaged to mono.
fn read_wav_mono(path: &str) -> std::io::Result<(Vec<f32>, u32)> {
    use std::io::{BufReader, Read, Seek, SeekFrom};

    let file   = std::fs::File::open(path)?;
    let mut rd = BufReader::new(file);

    let mut tag = [0u8; 4];
    rd.read_exact(&mut tag)?;
    if &tag != b"RIFF" {
        return Err(std::io::Error::new(std::io::ErrorKind::InvalidData, "not a RIFF file"));
    }
    let _ = rd_u32le(&mut rd)?;
    rd.read_exact(&mut tag)?;
    if &tag != b"WAVE" {
        return Err(std::io::Error::new(std::io::ErrorKind::InvalidData, "RIFF is not WAVE"));
    }

    let mut sample_rate: u32 = 0;
    let mut channels:    u16 = 0;
    let mut bits:        u16 = 0;
    let mut data:        Vec<u8> = Vec::new();

    loop {
        let mut id = [0u8; 4];
        if rd.read_exact(&mut id).is_err() { break; }
        let size = rd_u32le(&mut rd)?;
        match &id {
            b"fmt " => {
                let fmt = rd_u16le(&mut rd)?;
                if fmt != 1 {
                    return Err(std::io::Error::new(
                        std::io::ErrorKind::InvalidData, "only PCM WAV supported"));
                }
                channels    = rd_u16le(&mut rd)?;
                sample_rate = rd_u32le(&mut rd)?;
                let _ = rd_u32le(&mut rd)?;
                let _ = rd_u16le(&mut rd)?;
                bits        = rd_u16le(&mut rd)?;
                if size > 16 {
                    rd.seek(SeekFrom::Current((size - 16) as i64))?;
                }
            }
            b"data" => {
                data.resize(size as usize, 0);
                rd.read_exact(&mut data)?;
                break;
            }
            _ => { rd.seek(SeekFrom::Current(size as i64 + (size & 1) as i64))?; }
        }
    }

    if sample_rate == 0 || channels == 0 || bits == 0 || data.is_empty() {
        return Err(std::io::Error::new(std::io::ErrorKind::InvalidData, "incomplete WAV"));
    }

    let bps  = (bits / 8) as usize;
    let frsz = bps * channels as usize;
    let n    = data.len() / frsz;
    let mut out = Vec::with_capacity(n);
    for f in 0..n {
        let mut sum = 0f32;
        for ch in 0..channels as usize {
            let o = f * frsz + ch * bps;
            sum += match bits {
                8  => (data[o] as f32 - 128.0) / 128.0,
                16 => i16::from_le_bytes([data[o], data[o+1]]) as f32 / 32768.0,
                32 => i32::from_le_bytes(
                    [data[o], data[o+1], data[o+2], data[o+3]]) as f32 / 2_147_483_648.0,
                b  => return Err(std::io::Error::new(
                    std::io::ErrorKind::InvalidData,
                    format!("unsupported bit depth {b}"))),
            };
        }
        out.push(sum / channels as f32);
    }
    Ok((out, sample_rate))
}

/// Write a mono 16-bit PCM WAV.
fn write_wav_mono(path: &str, samples: &[i16], rate: u32) -> std::io::Result<()> {
    let channels:    u16 = 1;
    let bits:        u16 = 16;
    let block_align: u16 = channels * (bits / 8);
    let byte_rate:   u32 = rate * block_align as u32;
    let data_bytes:  u32 = samples.len() as u32 * block_align as u32;
    let chunk_size:  u32 = 36 + data_bytes;

    let mut f = std::fs::File::create(path)?;
    f.write_all(b"RIFF")?;
    wle32(&mut f, chunk_size)?;
    f.write_all(b"WAVE")?;
    f.write_all(b"fmt ")?;
    wle32(&mut f, 16)?;
    wle16(&mut f, 1)?;
    wle16(&mut f, channels)?;
    wle32(&mut f, rate)?;
    wle32(&mut f, byte_rate)?;
    wle16(&mut f, block_align)?;
    wle16(&mut f, bits)?;
    f.write_all(b"data")?;
    wle32(&mut f, data_bytes)?;
    let mut buf = Vec::with_capacity(samples.len() * 2);
    for &s in samples { buf.extend_from_slice(&s.to_le_bytes()); }
    f.write_all(&buf)
}

#[inline] fn wle16(f: &mut std::fs::File, v: u16) -> std::io::Result<()> { f.write_all(&v.to_le_bytes()) }
#[inline] fn wle32(f: &mut std::fs::File, v: u32) -> std::io::Result<()> { f.write_all(&v.to_le_bytes()) }
#[inline] fn rd_u16le<R: std::io::Read>(r: &mut R) -> std::io::Result<u16> {
    let mut b = [0u8; 2]; r.read_exact(&mut b)?; Ok(u16::from_le_bytes(b))
}
#[inline] fn rd_u32le<R: std::io::Read>(r: &mut R) -> std::io::Result<u32> {
    let mut b = [0u8; 4]; r.read_exact(&mut b)?; Ok(u32::from_le_bytes(b))
}
